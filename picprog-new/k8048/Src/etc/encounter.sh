#!/bin/bash
#
# A CLOSE ENCOUNTER
#
kio reset 1>/dev/null
while test 1;
do
	# C
	i2cset -y 1 0x40 0x00 60
	i2cset -y 1 0x40 0x01 72
	i2cset -y 1 0x40 0x02 48
	sleep 0.8

	# D
	i2cset -y 1 0x40 0x00 62
	i2cset -y 1 0x40 0x01 74
	i2cset -y 1 0x40 0x02 50
	sleep 0.8

	# A#
	i2cset -y 1 0x40 0x00 58
	i2cset -y 1 0x40 0x01 70
	i2cset -y 1 0x40 0x02 46
	sleep 0.8

	# A#
	i2cset -y 1 0x40 0x00 46
	i2cset -y 1 0x40 0x01 58
	i2cset -y 1 0x40 0x02 34
	sleep 0.8

	# F
	i2cset -y 1 0x40 0x00 53
	i2cset -y 1 0x40 0x01 65
	i2cset -y 1 0x40 0x02 41
	sleep 0.8

	# OFF
	i2cset -y 1 0x40 0x00 0
	i2cset -y 1 0x40 0x01 0
	i2cset -y 1 0x40 0x02 0
	sleep 0.8
done
exit 0
