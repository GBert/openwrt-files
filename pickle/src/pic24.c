		pic24_conf.config[PIC24_PART_LOWER][PIC24_FBTSEQ] = pic24_table_read24_post_increment();

		/* CONFIG upper */
		pic24_set_read_pointer(pic24_map[pic24_index].configlower | PIC24_CODE_UPPER);
		for (uint32_t i = 0; i < pic24_map[pic24_index].nconfig; ++i)
			pic24_conf.config[PIC24_PART_UPPER][i] = pic24_table_read16_post_increment();

		/* FBTSEQ upper */
		pic24_set_read_pointer(pic24_map[pic24_index].configlower | PIC24_CODE_UPPER | PIC24_FBTSEQ);
		pic24_conf.config[PIC24_PART_UPPER][PIC24_FBTSEQ] = pic24_table_read24_post_increment();
