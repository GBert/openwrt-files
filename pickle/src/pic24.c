/*
 * DETERMINE MEMORY REGION: CODE, PARTITION, DATA, CONFIG, EXEC, OR ID
 *
 *  RETURN PIC_REGIONCODE:
 *	000000 .. FLASH - 2
 *
 *  RETURN PIC_REGIONPART:
 *      400000 .. FLASH - 2
 *
 *  RETURN PIC_REGIONDATA:
 *	7FFXXX .. 7FFFFE
 *
 *  RETURN PIC_REGIONCONFIG:
 *	F80000 .. F8000E
 *	XXXXXX .. XXXXXX
 *
 *  RETURN PIC_REGIONEXEC:
 *	800000 .. 80XXXX
 *
 *  RETURN PIC_REGIONID:
 *	8005C0 .. 8005FE
 */
uint32_t
pic24_getregion(uint32_t address)
{
	/* CODE */
	if (pic24_wants_partition()) {
		/* CODE LOWER */
		if (address < pic24_map[pic24_index].flash) {
			return PIC_REGIONCODE;
		}
		/* CODE UPPER */
		if (address >= PIC24_CODE_UPPER && address < (PIC24_CODE_UPPER + pic24_map[pic24_index].flash)) {
			return PIC_REGIONPART;
		}
	} else {
		/* CODE */
		if (address < (pic24_map[pic24_index].flash << 1)) {
			return PIC_REGIONCODE;
		}
	}
	/* FBOOT */
	if (pic24_supports_partition() && (address == pic24_map[pic24_index].fbootaddr)) {
		return PIC_REGIONBOOT;
	}
	/* EEPROM */
	if (pic24_map[pic24_index].eeprom) {
		uint32_t data_low = PIC24_EXEC_LOW - pic24_map[pic24_index].eeprom;
		if (address >= data_low && address < PIC24_EXEC_LOW) {
			return PIC_REGIONDATA;
		}
	}
	/* EXEC */
	if ((pic24_map[pic24_index].datasheet == DS70102H) ||		/* dsPIC30F      */
		(pic24_map[pic24_index].datasheet == DS70284B)) {	/* dsPIC30F SMPS */
		if (address >= PIC24_EXEC_LOW && address < dsPIC30F_EXEC_HIGH) {
			return PIC_REGIONEXEC;
		}
	} else if ((pic24_map[pic24_index].datasheet == DS70663D) || 	/* dsPIC33EP/PIC24EP */
		(pic24_map[pic24_index].datasheet == DS70005137F) ||	/* dsPIC33EV         */
		(pic24_map[pic24_index].datasheet == DS70005160C) ||	/* dsPIC33EPXXGS5XX  */
		(pic24_map[pic24_index].datasheet == DS70005192B) || 	/* dsPIC33EPXXGS2XX  */
		(pic24_map[pic24_index].datasheet == DS70005256A)) {	/* dsPIC33EPXXGS7/8  */
		if (address >= PIC24_EXEC_LOW && address < dsPIC33E_EXEC_HIGH) {
			return PIC_REGIONEXEC;
		}
	} else if ((pic24_map[pic24_index].datasheet == DS30010073A) || /* PIC24FJXXGA4/GB4 */
		(pic24_map[pic24_index].datasheet == DS30010057F) ||	/* PIC24FJXXGA6/GB6 */
		(pic24_map[pic24_index].datasheet == DS30010102A)) {	/* PIC24FJXXGA7     */
		if (address >= PIC24_GAB_EXEC_LOW && address < PIC24_GA7_EXEC_HIGH) {
			return PIC_REGIONEXEC;
		}
	} else if (address >= PIC24_EXEC_LOW && address < PIC24_EXEC_HIGH) {
		return PIC_REGIONEXEC;
	}
	/* FUID */
	if (pic24_has_fuid()) {
		uint32_t fuid_high = pic24_map[pic24_index].fuidaddr + (pic24_map[pic24_index].nfuid << 1);
		if (address >= pic24_map[pic24_index].fuidaddr && address < fuid_high) {
			return PIC_REGIONID;
		}
	}
	/* CONFIG */
	if (pic24_has_config()) {
		uint32_t config_high = pic24_map[pic24_index].configaddr + (pic24_map[pic24_index].nconfig << 1);
		if (address >= pic24_map[pic24_index].configaddr && address < config_high) {
			return PIC_REGIONCONFIG;
		}
	}
	if (p.f)
		fprintf(p.f, "%s: warning: address unsupported [%06X]\n", __func__, address);
	return PIC_REGIONNOTSUP;
}

/*
 * INIT REGION FOR WRITING (CONFIG DELAYED)
 *
 *  RETURN REGION IF WRITING SUPPORTED
 */
static inline uint32_t
pic24_init_writeregion(uint32_t region)
{
	switch (region) {
	case PIC_REGIONCODE:
	case PIC_REGIONPART:
	case PIC_REGIONEXEC:
		if (pic24_map[pic24_index].codepanelsize == 1) { /* dsPIC33FJ WORD WRITING */
			/* TBLPAG = 0x00 */
			pic24_six(0x200001, 1);/* MOV #0x00, W1  */
			pic24_six(0x880191, 0);/* MOV W1, TBLPAG */
		}
		pic_write_panel(PIC_PANEL_BEGIN, region, pic24_map[pic24_index].codepanelsize);
		return region;
	case PIC_REGIONDATA:
		if (pic24_map[pic24_index].datapanelsize == 1) { /* PIC24F KM/KL WORD WRITING */
			/* TBLPAG = 0x7F */
			pic24_six(0x2007F1, 1); /* MOV #0x7F, W1  */
			pic24_six(0x880191, 0); /* MOV W1, TBLPAG */
		}
		pic_write_panel(PIC_PANEL_BEGIN, region, pic24_map[pic24_index].datapanelsize);
	case PIC_REGIONCONFIG:
	case PIC_REGIONID:
	case PIC_REGIONBOOT:
		return region;
	}
	if (p.f)
		fprintf(p.f, "%s: warning: region unsupported [%d]\n", __func__, region);
	return PIC_REGIONNOTSUP;
}

/*
 * WRITE REGION (CACHE CONFIG & FUID WORDS)
 */
static inline void
pic24_writeregion(uint32_t address, uint32_t region, uint32_t data)
{
	switch (region) {
	case PIC_REGIONCODE:
	case PIC_REGIONPART:
	case PIC_REGIONDATA:
	case PIC_REGIONEXEC:
		pic_write_panel(PIC_PANEL_UPDATE, address >> 1, data);
		return;
	case PIC_REGIONCONFIG:
		{
		uint32_t i = (address - pic24_map[pic24_index].configaddr) >> 1;
		pic24_conf.config[PIC24_PART_UPPER][i] =
			(data & PIC24_CONFIG_MASK) | PIC24_CONFIG_PENDING;
		}
		return;
	case PIC_REGIONID:
		{
		uint32_t i = (address - pic24_map[pic24_index].fuidaddr) >> 1;
		pic24_conf.fuid[i] = (data & PIC24_CONFIG_MASK) | PIC24_CONFIG_PENDING;
		}
	case PIC_REGIONBOOT: /* IGNORED: COMMAND LINE ONLY */
		return;
	}
	if (p.f)
		fprintf(p.f, "%s: warning: region unsupported [%d]\n", __func__, region);
}

/*
 * INIT REGION FOR VERIFY
 *
 *  RETURN REGION IF VERIFY SUPPORTED
 */
static inline uint32_t
pic24_init_verifyregion(uint32_t region)
{
	if (region != PIC_REGIONNOTSUP) {
		return region;
	}
	if (p.f)
		printf("%s: warning: region unsupported [%d]\n", __func__, region);
	return PIC_REGIONNOTSUP;
}

/*
 * VERIFY REGION
 *
 *  RETURN DATA
 */
static inline uint32_t
pic24_verifyregion(uint32_t address, uint32_t region, uint16_t index, uint32_t wdata)
{
	uint32_t vdata = 0;

	switch (region) {
	case PIC_REGIONNOTSUP:
		if (p.f)
			fprintf(p.f, "%s: warning: region unsupported [%d]\n",
				__func__, region);
	case PIC_REGIONCONFIG: /* UNSUPPORTED: BIT MASKS NEEDED  */
	case PIC_REGIONBOOT:   /* IGNORED:     COMMAND LINE ONLY */
		return wdata;
	}
	if (index == 0) {
		pic24_set_read_pointer(address);
		pic24_goto200();
	}
	vdata = pic24_table_read24_post_increment();
	if (vdata != wdata && p.f) {
		fprintf(p.f, "%s: error: read [%06X] expected [%06X] at [%06X]\n",
			__func__, vdata, wdata, address);
	}
	return vdata;
}

