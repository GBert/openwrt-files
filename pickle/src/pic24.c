	case DS70102H: /* dsPIC30F      */
	case DS70284B: /* dsPIC30F SMPS */
		pic24_dsPIC30F_erase(200000);
		break;
	case DS39768D:	  /* PIC24FJ          */
	case DS39934B:	  /* PIC24FJXXGA1/GB0 */
	case DS30009907C: /* PIC24FJXXGA1/GB1 */
		pic24_PIC24FJ_erase(0x880190);
		break;
	case DS30000510E: /* PIC24FJXXGA2/GB2 */
	case DS39970E:    /* PIC24FJXXDA1/DA2/GB2/GA3/GC0 */
		pic24_PIC24FJ_erase(0x8802A0);
		break;
	case DS30010073A: /* PIC24FJXXGA4/GB4 */
	case DS30010057F: /* PIC24FJXXGA6/GB6 */
	case DS30010102A: /* PIC24FJXXGA7     */
		pic24_PIC24FJ_GAB_erase();
		break;
	case DS70659C: /* dsPIC33FJ         */
	case DS75012B: /* PIC24FJXXMC       */
		pic24_dsPIC33FJ_erase();
		break;
	case DS39919C: /* PIC24F KA         */
	case DS30625D: /* PIC24F KM/KL      */
		pic24_PIC24FKA_erase();
		break;
